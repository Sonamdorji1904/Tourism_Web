                                    <li>Visit Chhimi Lhakhang (Temple of Fertility)</li>
                                    <li>Explore Woolakha Nunnery</li>
                                    <li>Visit Punakha Dzong</li>
                                    <li>Overnight in Punakha</li>
                                </ul>
